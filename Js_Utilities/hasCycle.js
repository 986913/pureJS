var hasCycle = function (head) {
  let slow = head;
  let fast = head;

  while (fast && fast.next) {
    slow = slow.next;
    fast = fast.next.next;
    if (slow === fast) return true; // if(slow === fast) is inside while loop, if they are equal means there is cycle
  }
  return false; // means no cycle
};
