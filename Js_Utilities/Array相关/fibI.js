const fib = (n) => {
  let dp = [0, 1];
  for (let i = 2; i <= n; i++) {
    dp[i] = dp[i - 1] + dp[i - 2];
  }
  // console.log(dp);
  return dp[n];
};

/* --------------------------------- Code solution 4:  Math (god tier) ------------------------- */
// Time: O(1)
// Space: O(1)
function fib(n) {
  const A = (1 + Math.sqrt(5)) / 2;
  const B = (1 - Math.sqrt(5)) / 2;
  const fib = (Math.pow(A, n) - Math.pow(B, n)) / Math.sqrt(5);
  return Math.floor(fib);
}
