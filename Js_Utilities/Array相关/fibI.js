// Time: O(2^n)
// Space: O(n)
function fib(n) {
  if (n < 2) return n;
  return fib(n - 1) + fib(n - 2);
}

/* --------------------------------- Code solution 2: üëçüëçüëçMemoized Recursion ------------------------ */
// Time: O(n)
// Space: O(n)
function fib(n) {
  const cache = {};

  function memo(n) {
    if (n < 2) return n;

    if (!cache[n]) {
      cache[n] = memo(n - 1) + memo(n - 2);
    }

    return cache[n];
  }

  return memo(n);
}

